public class TiposPrimitivos {

    public void func1(){
        ///A diferença principal entre cada um desses tipos primitivos é:
        ///A quantidade de bytes em memória que cada um ocupa

        byte inteiro1=10;
        short inteiro2=10;
        int inteiro3=10;
        long inteiro4=10;

        /*
        * Tabela tipos primitivos
        * Operador      | Tamanho
        * boolean       | 1 bit
        * byte          | 1 byte
        * short         | 2 bytes
        * char          | 2 bytes
        * int           | 4 bytes
        * float         | 4 bytes
        * long          | 8 bytes
        * double        | 8 bytes
        * */

       int numero = 4;
       int numeroIgual = numero;
       numero = numero+5;

       System.out.println(numero);
       System.out.println(numeroIgual);
       //Variaveis primitivas guardam copias do valor atribuidos a elas
       //Porem outros tipos de variaveis guardam referências ao valor atribuidos





    }
}
